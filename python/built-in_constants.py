#!/usr/bin/env python3

### 6 constants live in the built-in namespace.

print(type(False))
# <class 'bool'>

print(type(True))
# <class 'bool'>

print(type(None))
# <class 'NoneType'>

print(type(NotImplemented))
# <class 'NotImplementedType'>

print(type(Ellipsis))
# <class 'ellipsis'>

print(type(__debug__))
# <class 'bool'>

